
import math

class Calc:

    def add(self,x, y):
        print(f"Answer: {x + y}")

    def subtract(self,x, y):
        print(f"Answer: {x - y}")
    
    def multiply(self,x, y):
        print(f"Answer: {x * y}")

    def divide(self,x, y):
        print(f"Answer: {x / y}")

    def exponent(self,x, y):
        print(f"Answer: {x ** y}")
